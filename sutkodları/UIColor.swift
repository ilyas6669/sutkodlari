import UIKit

extension UIColor {
    class func customBlue() -> UIColor {
        return UIColor(red: 19/255, green: 201/255, blue: 208/255, alpha: 1)
    }
}


extension UIColor {
    class func customBackground() -> UIColor {
        return UIColor(red: 233/255, green: 241/255, blue: 253/255, alpha: 1)
    }
}

extension UIColor {
    class func customBackground2() -> UIColor {
        return UIColor(red: 241/255, green: 244/255, blue: 250/255, alpha: 1)
    }
}

extension UIColor {
    class func customTabBar() -> UIColor {
        return UIColor(red: 232/255, green: 235/255, blue: 244/255, alpha: 1)
    }
}

